public enum EstadosActivacion {

    ACTIVO(1),  //ESTADO ACTIVO
    INACTIVO(2); //ESTADO INACTIVO

    private int estado;

    EstadosActivacion(int estado){
        this.estado = estado;
    }

    public byte getValue() {
        return (byte)estado;
    }
}
