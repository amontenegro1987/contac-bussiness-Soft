public enum TiposPersona {

    PERSONA_NATURAL (1),
    PERSONA_JURIDICA (2);

    private int tipoPersona;

    TiposPersona(int tipoPersona){
        this.tipoPersona = tipoPersona;
    }

    public int getValue(){
        return tipoPersona;
    }
}
