public class ProductoNoEncontradoException extends ManagerProductoServiceBusinessException {

    /**
     * Create exception with entity identifier
     * @param id, Identifier object
     */
    public ProductoNoEncontradoException(int id) {
        super("No se encontro producto con id: " + id);
    }

    /**
     * Create exception with entity criteria
     * @param criteria, String
     */
    public ProductoNoEncontradoException(String criteria) {
        super("No se encontro producto con criterio: " + criteria);
    }
}
