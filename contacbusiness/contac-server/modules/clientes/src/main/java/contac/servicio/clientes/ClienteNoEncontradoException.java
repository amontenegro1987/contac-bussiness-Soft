public class ClienteNoEncontradoException extends ManagerClientesServiceBusinessException {

    /**
     * Constructor error message with UID
     *
     * @param id, Identifier
     */
    public ClienteNoEncontradoException(int id) {
        super("No se encontro cliente con id: " + id);
    }

    /**
     * Constructor error message with Criteria
     *
     * @param criterio, Criteria
     */
    public ClienteNoEncontradoException(String criterio) {
        super("No se encontro cliente con criterio: " + criterio);
    }


}
