package contac.administracion.usuario.app;

import contac.modelo.entity.Rol;

/**
 * Representa un modelo de roles activos para JTable
 * User: Eddy
 * Date: 7/31/11
 * Time: 11:49 AM
 */
public class RolesActivoApp {

    /**
     * Identificador del rol
     */
    private Integer id;
    /**
     * Nombre del rol
     */
    private String rol;
    /**
     * Descripcion del rol
     */
    private String descripcion;
    /**
     * Entidad del rol
     */
    private Rol entity;
    /**
     * Estado del rol
     */
    private boolean activo;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getRol() {
        return rol;
    }

    public void setRol(String rol) {
        this.rol = rol;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public Rol getEntity() {
        return entity;
    }

    public void setEntity(Rol entity) {
        this.entity = entity;
    }

    public boolean isActivo() {
        return activo;
    }

    public void setActivo(boolean activo) {
        this.activo = activo;
    }
}
